#!/usr/bin/python3

def add(a, b):
    """Return the sum of two integers."""
    return a + b

def sub(a, b):
    """Return the result of subtracting b from a."""
    return a - b

def mul(a, b):
    """Return the product of two integers."""
    return a * b

def div(a, b):
    """Return the result of dividing a by b as an integer."""
    return int(a / b)
